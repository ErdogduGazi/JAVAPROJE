    private void cikarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cikarBtnActionPerformed
          
          int selection= lbxUyeler.getSelectedIndex(); 
 
        if (selection==-1) {
            JOptionPane.showMessageDialog(this, "Lütfen Tüm Alanları Doldurun", "İşlem Başarısız", JOptionPane.PLAIN_MESSAGE);

        } else {
            String uyeAdSoyadType = lbxUyeler.getSelectedItem();
            dbIslemleri.uyeCikar(uyeAdSoyadType);
            uyeGoruntule();
        }

    }//GEN-LAST:event_cikarBtnActionPerformed

