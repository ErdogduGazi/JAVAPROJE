    private void uyeEkleBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uyeEkleBtnActionPerformed

        if (uyeAdiArea.getText().isEmpty() || uyeSoyadiArea.getText().isEmpty() || uyeOkunanKitapArea.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Lütfen Tüm Alanları Doldurun", "İşlem Başarısız", JOptionPane.PLAIN_MESSAGE);

        } else {
            String uyeAdi = uyeAdiArea.getText();
            String uyeSoyadi = uyeSoyadiArea.getText();
            int okunanKitapSayisi = Integer.parseInt(uyeOkunanKitapArea.getText());

            dbIslemleri.uyeEkle(uyeAdi, uyeSoyadi, okunanKitapSayisi);

            this.uyeGoruntule();
        }


    }//GEN-LAST:event_uyeEkleBtnActionPerformed

