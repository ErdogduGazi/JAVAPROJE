    public void uyeGoruntule() {
        lbxUyeler.clear();
        ArrayList<Uye> uyelerim = dbIslemleri.uyeleriGetir();

        for (Uye uye : uyelerim) {
            //uyeleri listeye yaz..
            lbxUyeler.add(uye.getUyeAdi() + " " + uye.getUyeSoyadi() + " " + uye.getUyelikTipi());
        }

    }

